const concat = require('./index');

test('[1], [2, [3], [[4]]] -> [1, 2, 3, [4]]', () => {
	expect(concat([1], [2, [3], [[4]]])).toEqual([1, 2, 3, [4]]);
});

test('[1], [2, [[3,4,6],6,7], [[4]]] ->  [1, 2, [3, 4, 6], 6, 7, [4]]', () => {
	expect(concat([1], [2, [[3, 4, 6], 6, 7], [[4]]])).toEqual([1, 2, [3, 4, 6], 6, 7, [4]]);
});